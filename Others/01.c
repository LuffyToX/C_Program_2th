/*
   <计算平均值>
   
   问题描述：从键盘输入三个整数(分别存入 x, y, z), 计算并输出三个数的和以及平均值
   输入格式：从键盘输入三个整数, 整数之间以空格隔开
   输出格式：在屏幕上分两行显示结果：第一行为三个数的和，整数形式输出;
                                  第二行为三个数的平均值，浮点数形式输出，小数点后保留两位小数
   输入样例：3 2 3
   输出样例：8
            2.67
*/

#include<stdio.h>
#include<stdlib.h>

int main(){
	int a, b, c;
	scanf("%d %d %d", &a, &b, &c);
	printf("%d\n", a+b+c);
	printf("%.2f\n", (a+b+c) / 3.0);

	system("pause");
	return 0;
}
